public class MathLogic implements Math{
    private int num1=10;
    private int num2=20;

    public int add(){
        return num1+num2;
    }

    public int subtract(){
        return num1-num2;
    }

    public int multiply(){
        return num1*num2;
    }

    public double divide(){
        return num1/num2;
    }
}
