package com.transflower.tflcomentor.entities;


public class Concept {

    private Long id;
    private String name;

    public Concept() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
